// Built-in browser functions
// the replace() string function takes a string,
// replaces one substring with another, and returns
// a new string with the replacement made

var myText = 'I am a string';
var newString = myText.replace('string', 'sausage');
console.log(newString);

// My example of replace() string function
var myPet = "I have a dog.";
console.log(myPet);
var newPet = myPet.replace("dog", "rat");
console.log(newPet);

// Another example of replace() string function
var mySibling = "I have two brothers.";
console.log(mySibling);
var newSibling = mySibling.replace("two", "three");
console.log(newSibling);